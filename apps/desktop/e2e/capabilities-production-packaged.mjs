async function selectTargetButton(page, app, targetUrl, expectedSelection = 'Selected Run capability') {
  await click(page.getByRole('button', { name: 'Select preview element' }), 'select preview element')
  await expectVisible(page.getByRole('button', { name: 'Click an element…' }), 'active target element picker')
  // React renders the active state before the async preload invocation has
  // necessarily crossed IPC and installed the guest's native mouse listener.
  // A user naturally takes longer than this to move into the Preview; retain
  // that real interaction ordering in the automated packaged journey.
  await page.waitForTimeout(300)
  const preview = page.getByLabel('Target application Preview')
  const frame = preview.locator('webview')
  const box = await frame.boundingBox()
  if (!box) throw new Error('Target preview webview has no visible bounds')
  // Read only the rendered webview's Electron identity, resolve the marked
  // control's actual guest box, and send a native WebContents mouse event.
  // The production picker observes that real guest input; this driver never
  // injects workflow state, calls the bridge, or dispatches IPC.
  const guestId = await frame.evaluate((element) => element.getWebContentsId())
  const clickResult = await app.evaluate(async ({ webContents }, input) => {
    const deadline = Date.now() + 30_000
    let lastState = 'guest unavailable'
    while (Date.now() < deadline) {
      const guest = webContents.fromId(input.guestId)
        ?? webContents.getAllWebContents().find((contents) => contents.getURL().startsWith(input.targetUrl))
      if (!guest || guest.isDestroyed()) {
        await new Promise((resolve) => setTimeout(resolve, 100))
        continue
      }
      lastState = `guest ${guest.id} at ${guest.getURL() || '(no URL)'}`
      if (!guest.getURL().startsWith(input.targetUrl)) {
        await new Promise((resolve) => setTimeout(resolve, 100))
        continue
      }
      try {
        if (!guest.debugger.isAttached()) guest.debugger.attach('1.3')
        await guest.debugger.sendCommand('DOM.enable')
        const { root } = await guest.debugger.sendCommand('DOM.getDocument', { depth: 1, pierce: true })
        const { nodeId } = await guest.debugger.sendCommand('DOM.querySelector', {
          nodeId: root.nodeId,
          selector: '[data-cap-id="run-capability"]',
        })
        if (!nodeId) {
          const { outerHTML = '' } = await guest.debugger.sendCommand('DOM.getOuterHTML', { nodeId: root.nodeId })
          const documentPreview = outerHTML.replace(/\s+/g, ' ').slice(0, 600)
          lastState = `${lastState}; marked control not ready yet; mainFrameLoading=${guest.isLoadingMainFrame()}; document=${documentPreview || '(empty)'}`
          await new Promise((resolve) => setTimeout(resolve, 100))
          continue
        }
        const { model } = await guest.debugger.sendCommand('DOM.getBoxModel', { nodeId })
        const x = (model.content[0] + model.content[2] + model.content[4] + model.content[6]) / 4
        const y = (model.content[1] + model.content[3] + model.content[5] + model.content[7]) / 4
        guest.focus()
        guest.sendInputEvent({ type: 'mouseMove', x, y })
        guest.sendInputEvent({ type: 'mouseDown', x, y, button: 'left', clickCount: 1 })
        await new Promise((resolve) => setTimeout(resolve, 100))
        guest.sendInputEvent({ type: 'mouseUp', x, y, button: 'left', clickCount: 1 })
        return { clicked: true, detail: lastState }
      } catch (error) {
        lastState = `${lastState}; ${error instanceof Error ? error.message : String(error)}`
        await new Promise((resolve) => setTimeout(resolve, 100))
      }
    }
    return { clicked: false, detail: lastState }
  }, { guestId, targetUrl })
  if (!clickResult.clicked) {
    const pickerError = await page.locator('.preview-binding-picker [role="alert"]').textContent().catch(() => '')
    throw new Error(`Target preview control was not available: ${clickResult.detail}${pickerError ? `; UI reported: ${pickerError}` : ''}`)
  }
  await waitForStatus(page, expectedSelection)
}

